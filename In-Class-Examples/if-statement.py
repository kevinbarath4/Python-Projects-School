# to be able to compare the numbers, need to convert them from strings to a numeric data type
firstNum = float(input("Please enter the 1st number: "))
secondNum = float(input("Please enter the 2nd number: "))

if firstNum > secondNum:
    # the line below will run only if the Boolean expression in if statement evaluates to True
    print("1st number is greater than 2nd number")