fruit = ("apple", "banana", "cherry")

i = 0		            # initializing a variable that will be used as the loop counter 
                        # and as tuple index

# while i <= len(fruit) - 1:
while i < len(fruit):

    print(fruit[i])     # using i as the index, to reference the tuple element in the current iteration
    i += 1		        # incrementing the loop counter variable