fruit = ["apple", "banana", "cherry", "lemon", "watermelon", "cherry"]

print(fruit[1])             # Prints the second item in the tuple
print(fruit[-1])            # Negative indexing means start from the end; prints out the last item
print(fruit[2:5])           # Using range in index: will print out the items located at position 3, 4, and 5.
                            # The search tarts at index 2 (included) and ends at index 5 (not included).